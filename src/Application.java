
public class Application {// here we will look at for loops!

	public static void main(String[] args) {
		for (int i = 0; i <= 10; i++) {// this loop will count from 0-10 syntax is very similar to JS!
			System.out.println("The value of i is now " + i);// the \n character prints a new line!
			System.out.printf("The value of i is now %d \n", i);// this is like println but as a format specifyer, i.e. replace all %something with somethingElse
		}
	}

}
